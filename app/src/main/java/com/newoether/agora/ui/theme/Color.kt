// ── Public API ──
enum class ColorSchemePreset { DEFAULT, OCEAN, FOREST, SUNSET, ROSE, SLATE }

data class ColorSchemePair(val light: ColorScheme, val dark: ColorScheme)

fun colorSchemeForPreset(preset: ColorSchemePreset): ColorSchemePair = when (preset) {
    ColorSchemePreset.DEFAULT -> ColorSchemePair(
        buildLightScheme(PurplePrimaryLight, PurpleOnPrimaryLight, PurplePrimaryContainerLight, PurpleOnPrimaryContainerLight, PurpleSecondaryLight, PurpleOnSecondaryLight, PurpleSecondaryContainerLight, PurpleOnSecondaryContainerLight, PurpleTertiaryLight, PurpleOnTertiaryLight, PurpleTertiaryContainerLight, PurpleOnTertiaryContainerLight),
        buildDarkScheme(PurplePrimaryDark, PurpleOnPrimaryDark, PurplePrimaryContainerDark, PurpleOnPrimaryContainerDark, PurpleSecondaryDark, PurpleOnSecondaryDark, PurpleSecondaryContainerDark, PurpleOnSecondaryContainerDark, PurpleTertiaryDark, PurpleOnTertiaryDark, PurpleTertiaryContainerDark, PurpleOnTertiaryContainerDark),
    )
    ColorSchemePreset.OCEAN -> ColorSchemePair(
        buildLightScheme(OceanPrimaryLight, OceanOnPrimaryLight, OceanPrimaryContainerLight, OceanOnPrimaryContainerLight, OceanSecondaryLight, OceanOnSecondaryLight, OceanSecondaryContainerLight, OceanOnSecondaryContainerLight, OceanTertiaryLight, OceanOnTertiaryLight, OceanTertiaryContainerLight, OceanOnTertiaryContainerLight),
        buildDarkScheme(OceanPrimaryDark, OceanOnPrimaryDark, OceanPrimaryContainerDark, OceanOnPrimaryContainerDark, OceanSecondaryDark, OceanOnSecondaryDark, OceanSecondaryContainerDark, OceanOnSecondaryContainerDark, OceanTertiaryDark, OceanOnTertiaryDark, OceanTertiaryContainerDark, OceanOnTertiaryContainerDark),
    )
    ColorSchemePreset.FOREST -> ColorSchemePair(
        buildLightScheme(ForestPrimaryLight, ForestOnPrimaryLight, ForestPrimaryContainerLight, ForestOnPrimaryContainerLight, ForestSecondaryLight, ForestOnSecondaryLight, ForestSecondaryContainerLight, ForestOnSecondaryContainerLight, ForestTertiaryLight, ForestOnTertiaryLight, ForestTertiaryContainerLight, ForestOnTertiaryContainerLight),
        buildDarkScheme(ForestPrimaryDark, ForestOnPrimaryDark, ForestPrimaryContainerDark, ForestOnPrimaryContainerDark, ForestSecondaryDark, ForestOnSecondaryDark, ForestSecondaryContainerDark, ForestOnSecondaryContainerDark, ForestTertiaryDark, ForestOnTertiaryDark, ForestTertiaryContainerDark, ForestOnTertiaryContainerDark),
    )
    ColorSchemePreset.SUNSET -> ColorSchemePair(
        buildLightScheme(SunsetPrimaryLight, SunsetOnPrimaryLight, SunsetPrimaryContainerLight, SunsetOnPrimaryContainerLight, SunsetSecondaryLight, SunsetOnSecondaryLight, SunsetSecondaryContainerLight, SunsetOnSecondaryContainerLight, SunsetTertiaryLight, SunsetOnTertiaryLight, SunsetTertiaryContainerLight, SunsetOnTertiaryContainerLight),
        buildDarkScheme(SunsetPrimaryDark, SunsetOnPrimaryDark, SunsetPrimaryContainerDark, SunsetOnPrimaryContainerDark, SunsetSecondaryDark, SunsetOnSecondaryDark, SunsetSecondaryContainerDark, SunsetOnSecondaryContainerDark, SunsetTertiaryDark, SunsetOnTertiaryDark, SunsetTertiaryContainerDark, SunsetOnTertiaryContainerDark),
    )
    ColorSchemePreset.ROSE -> ColorSchemePair(
        buildLightScheme(RosePrimaryLight, RoseOnPrimaryLight, RosePrimaryContainerLight, RoseOnPrimaryContainerLight, RoseSecondaryLight, RoseOnSecondaryLight, RoseSecondaryContainerLight, RoseOnSecondaryContainerLight, RoseTertiaryLight, RoseOnTertiaryLight, RoseTertiaryContainerLight, RoseOnTertiaryContainerLight),
