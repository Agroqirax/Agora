        buildLightScheme(SlatePrimaryLight, SlateOnPrimaryLight, SlatePrimaryContainerLight, SlateOnPrimaryContainerLight, SlateSecondaryLight, SlateOnSecondaryLight, SlateSecondaryContainerLight, SlateOnSecondaryContainerLight, SlateTertiaryLight, SlateOnTertiaryLight, SlateTertiaryContainerLight, SlateOnTertiaryContainerLight),
